Title e Meta Description
Title e Meta Description são os principais locais para otimizar as keywords mais relevantes para a sua página. Afinal, essas são as primeiras coisas que o usuário vai ver antes mesmo de entrar em seu site. Nesses campos, utilize as palavras-chave mais importantes e com maior volume de busca. Além disso, crie uma chamada atraente para que o usuário entre no seu site.

Conteúdo
O texto da página precisa ser claro em relação ao assunto que você vai abordar. Isso facilitará tanto para o usuário quanto para os robôs de busca do Google, que, ao entrarem no site, saberão do que a página se trata. Quanto mais claro for o conteúdo, melhor.

Títulos e Subtítulos (tags h1, h2, h3…)
Os títulos e subtítulos são utilizados para dar melhor coesão e coerência ao seu conteúdo. Por isso, eles têm um peso importante na busca do Google. Neles, invista em palavras-chave que possuem um volume de buscas alto e que façam com que o texto fique mais fluido.

Palavras em Negrito (tag strong)
Insira as palavras-chave selecionadas da maneira mais natural possível. Caso não seja natural, simplesmente não otimize!
Assim, é garantido que o seu conteúdo tenha relevância para o leitor e que o Google identifique os principais termos no texto.

Caso você queira dar maior relevância para um termo específico, vale também deixar o termo em negrito usando a tag , mas utilize com moderação, pois isso pode prejudicar o site.

Imagens (tag alt)
Usar legendas claras nas imagens do seu site é muito importante não só para que o Google saiba do que se trata cada uma delas como também para a acessibilidade (já que as ferramentas utilizadas pelos deficientes visuais leem as imagens a partir do que está escrito na tag alt). Seja o mais descritivo possível e, se for natural, utilize as palavras-chave definidas para dar maior ênfase ao assunto tratado.

Se a página que você está otimizando é muito importante para o seu site, como a de categoria, por exemplo, vale a pena inseri-la no menu superior e no footer, utilizando o termo principal como título do link. Assim, ela recebe uma quantidade considerável de links internos, aumentando sua relevância e autoridade.

