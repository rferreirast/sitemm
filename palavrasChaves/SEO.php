Exemplo de como agrupar as palavras
Consideremos que, em vez de cinco palavras principais, você tenha apenas duas: tinta e  tintas. Essas são palavras difíceis e você provavelmente nunca parará de otimizá-las.

Além disso, você poderá combinar essas palavras-chave com qualquer cor existente. Entretanto, você determinará que terá um grupo com segundo nível de importância que seriam tintas das cores mais conhecidas: tinta amarela, tinta azul, tinta vermelha, tinta verde, etc.  Essas são palavras médias e você geralmente irá variar a palavra que está otimizando no momento.

O terceiro grupo seria o de todas as outras tintas. Essas são mais fáceis de se posicionar, mas por outro lado são muitas, e você deveria encontrar uma maneira de sempre otimizá-las.